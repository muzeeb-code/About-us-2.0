// ----- Apply translations to DOM -----
function applyTranslations(lang) {
  const dict = translations[lang];
  if (!dict) return;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      // Use innerHTML to preserve inline markup like <br> and <span> in headings
      el.innerHTML = dict[key];
    }
  });
}

// ----- Handler wired from HTML: onchange="handleLanguageChange(this)" -----
function handleLanguageChange(selectEl) {
  // Support both 'jp' (in your HTML) and 'ja' (ISO code)
  const raw = selectEl?.value || "en";
  const lang = raw === "jp" ? "ja" : raw;
  applyTranslations(lang);
}

